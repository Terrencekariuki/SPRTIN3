    <li>High-visibility vests: Enhance visibility, especially in areas with moving equipment.</li>
    <li>Guardrails: Installed along open edges to prevent falls.</li>
    <li>Safety nets: Positioned beneath elevated work areas to catch falling workers or objects.</li>
    <li> Harnesses and lanyards: Worn by workers at heights, attached to anchor points.</li>
